	<p>
		“You intended to harm me, but God intended it for good to accomplish what is now being done, the
		saving of many lives.” Genesis 50:20 NIV. Covid was bad, but God has used it to bring about good
		in my life. Covid-19 was discovered in December of 2019. It reached and started affecting people
		in the United States around February 2020. It has killed over five million people as of December
		2021 and has harmed many others. When Covid just started, the world went into lockdown to try to
		contain it, which has been unsuccessful (I will not go into whether it helped, but we can all
		agree that it didn’t stop the pandemic). This produced a large amount of isolation and fear,
		which affected everyone. After these past two years, some people might say that Covid brought no
		good to anyone, but I would say that God has used Covid for my good for the following reasons.
		He gave me good circumstances and timing, He gave me a challenge, and He strengthened my faith.
	</p>
	<p>
		One way God has used Covid for my good is that He gave me good circumstances and timing. I have
		supportive parents and a kind family who are enjoyable to hang out with during the lockdown. I
		was starting to mature, so God could really change me during the past few years, but I was
		mature enough to be able to dissect and understand the ideas and the changes around me which
		helped prevent confusion.
	</p>
	<p>
		Another way that God has used Covid for my good is that He gave me a challenge. When I face a
		challenge, it pushes me to try harder. As an example, when my youth group reimplemented masks,
		which hinder making friends and getting to know people, they actually had an opposite effect
		because I tried harder and actually talked to a new person. Lastly, God tells us in the Bible
		(through Paul) that “suffering produces perseverance; perseverance, character; and character,
		hope” (Romans 5:3b-4). Therefore Covid over time gave me hope.
	</p>
	<p>
		Lastly, He strengthened my faith. Covid took the world out of the world’s control. It took my
		circumstances out of my control, and showed me that I couldn’t plan anything absolutely. Using
		an unprecedented circumstance, He taught me to let go of my circumstances, which brought fear,
		but then brought me to the Lord.
	</p>
	<p>
		I am thankful to have been brought closer to the Lord through Covid, but not everyone responded
		to Covid in this way. One response I’ve seen is denial in response to fear, which brings more
		fear, instead of faith in response to fear, which brings peace. Because of that, some people
		have been turned further away from faith instead of towards it. I have been blessed with a
		faith-based response to fear, which has built me up and strengthened me, and I hope that Covid
		has built others' faith and can continue to strengthen people’s faith.
	</p>
	<p>
		Therefore, God has used Covid for my good because He gave me good circumstances and timing, He
		gave me a challenge, and He strengthened my faith. As Christians, our response to fear, and
		therefore Covid, should be faith. We shouldn’t continue living in fear, and we shouldn’t go into
		denial about it, instead we should have faith and let go. Paul reminds us of the results of
		suffering in faith: “Not only that, but we rejoice in our sufferings, knowing that suffering
		produces endurance, and endurance produces character, and character produces hope” Romans 5:3-4
		(ESV)
	</p>
</Article>
